#!/bin/sh

echo "EQ Set to 13dB"
sudo i2cset -f -y 2 0x48 0x32 0x99
sudo i2cset -f -y 2 0x48 0x33 0x99
sudo i2cset -f -y 7 0x48 0x32 0x99
sudo i2cset -f -y 7 0x48 0x33 0x99

echo "Pre-Emp OFF"
sudo i2cset -f -y 2 0x65 0x06 0xa0
sudo i2cset -f -y 7 0x65 0x06 0xa0
sleep 5
